package command.example1;

public class GarageDoor {
	public void up() {
		System.out.println("문을 올립니다.");
	}
	public void down() {
		System.out.println("문을 내립니다.");
	}
	public void stop() {
		System.out.println("문 동작을 정지합니다.");
	}
	public void lightOn() {
		System.out.println("불을 켭니다.");
	}
	public void lightOff() {
		System.out.println("불을 끕니다.");
	}
}
